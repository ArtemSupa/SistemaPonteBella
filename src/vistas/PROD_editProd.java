    private void cbox_categoriaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbox_categoriaItemStateChanged
       
       String query = cbox_categoria.getSelectedItem().toString();
       TableRowSorter<DefaultTableModel> tr = new TableRowSorter(tbl_grillaProd.getModel());
       tbl_grillaProd.setRowSorter(tr);
       
       if(query !="Seleccione una categoria"){
           tr.setRowFilter(RowFilter.regexFilter(query, 5));
       
       
       }else{
           tbl_grillaProd.setRowSorter(tr);
       }
    }//GEN-LAST:event_cbox_categoriaItemStateChanged

